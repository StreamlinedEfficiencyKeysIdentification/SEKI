class Equipamento {
  final String id;
  final String marca;
  final String modelo;
  final String qrcode;
  final String empresa;
  final String setor;
  final String usuario;
  final String criador;
  final String status;

  Equipamento({
    required this.id,
    required this.marca,
    required this.modelo,
    required this.qrcode,
    required this.empresa,
    required this.setor,
    required this.usuario,
    required this.criador,
    required this.status,
  });
}
